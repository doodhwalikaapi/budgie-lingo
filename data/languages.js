function getAllLanguages() {
  return ALL_LANGUAGES.map(l => ({ ...l, available: AVAILABLE.includes(l.code) }));
}

function isAvailable(code) {
  return AVAILABLE.includes(code);
}

function getLanguageName(code) {
  const lang = ALL_LANGUAGES.find(l => l.code === code);
  return lang ? lang.name : code;
}

module.exports = { getAllLanguages, isAvailable, getLanguageName, AVAILABLE };
